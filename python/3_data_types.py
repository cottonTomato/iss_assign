# There are various datatypes in python
# to work with various kinds of data, like
# numbers, words, raw bytes.

# Integer
a = 5
b = -58

# Float
c = 3.14

# Bool
d = False

# String
e = "Abhinav"
f = f"My name is {e}"  # f-string

# Lists (mutable)
g = [a, b, c, d]

# Tuples (immutable)
h = (a, b, c, d)

# Sets (all elements are unique)
i = {a, b, c, d}

# Dictionary (key-value pair)
j = {a: c, b: d}
